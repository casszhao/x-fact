        pooled_output = outputs[1]

        pooled_output = self.dropout(pooled_output)

        # Reshape back to [batch_size, num_evidences, emb_size]
        pooled_output = pooled_output.view(-1, num_evidences, outputs[1].size(-1))

        # Aggregation function
        pooled_output = torch.mean(pooled_output, 1, keepdim=False)

        logits = self.classifier_dict[source](pooled_output)

        outputs = (logits,) + outputs[2:]  # add hidden states and attention if they are here

        if labels is not None:
            if self.config_dict[source].num_labels == 1:
                #  We are doing regression
                loss_fct = MSELoss()
                loss = loss_fct(logits.view(-1), labels.view(-1))
            else:
                num_labels = self.config_dict[source].num_labels
                if self.config.criterion is None:
                    # Use CrossEntropy Loss
                    loss_fct = CrossEntropyLoss()
                    loss = loss_fct(logits.view(-1, num_labels), labels.view(-1))
                elif self.config.criterion.lower() == 'label_smoothing':
                    loss_fct = LabelSmoothedCrossEntropy(self.config.label_smoothing)
                    loss = loss_fct(logits.view(-1, num_labels), labels.view(-1))
                elif self.config.criterion.lower() == 'focal':
                    loss_fct = FocalLoss(self.config.focal_gamma)
                    loss = loss_fct(logits.view(-1, num_labels), labels.view(-1))
                else:
                    raise NotImplementedError

            outputs = (loss,) + outputs

        return outputs  # (loss), logits, (hidden_states), (attentions)

