class MultiTaskBertForSequenceClassification(BertPreTrainedModel):


    def __init__(self, config):
        #if type(config) == dict:
        self.config_dict = config
        self.sources = list(self.config_dict.keys())
        super().__init__(self.config_dict[self.sources[0]])

        self.bert = BertModel(self.config_dict[self.sources[0]])
        # All configs differ only by num labels, so we can use one of them to initialize encoder

        self.dropout = nn.Dropout(self.config_dict[self.sources[0]].hidden_dropout_prob)

        self.classifier_dict = nn.ModuleDict()
        for source in self.sources:
            self.classifier_dict[source] = nn.Linear(self.config_dict[source].hidden_size, self.config_dict[source].num_labels)


        self.init_weights()


